  /// カテゴリ名と術式名で全リストをフラットに検索
  static List<MapEntry<SurgeryCategory, String>> searchSurgeries(String query) {
    final results = <MapEntry<SurgeryCategory, String>>[];
    if (query.isEmpty) return results;

    final lowerQuery = query.toLowerCase();
    for (final entry in surgeryTypes.entries) {
      for (final surgery in entry.value) {
        if (surgery.toLowerCase().contains(lowerQuery)) {
          results.add(MapEntry(entry.key, surgery));
        }
      }
    }
    return results;
  }
